public class Variable04 {
	public static void main(String[] args) {
		float nFloat = 10.1f;  //float 값은 f를 적어줘야 함 
		double nDouble = 10.1d; // d는 써주는게 원칙이지만 안 적어도 됨, JAVA에서는 실수를 기본값으로 인식하기 때문에
		double nDouble2 = 10.1; 
		
//alt + ctrl + 위아래 방향 키: 줄복사 
//alt  + 위아래 방향 키: 줄이동 

		System.out.println("float 형: " + nFloat);
		System.out.println("double 형: " + nDouble);
		System.out.println("double 형: " + nDouble2);
	}
}
